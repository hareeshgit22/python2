"""This module does a calculator implementation"""
def add(x,y):
	"""Addition of 2 numers which are givrn as args and return sum"""
	return x+y

def sub(x,y):
	"""difference of 2 numers which are givrn as args and return sum"""
	return x-y


def mul(x,y):
	"""product of 2 numers which are givrn as args and return sum"""
	return x*y


def div(x,y):
	"""div of 2 numers which are givrn as args and return sum"""
	return x/y









